package gp.library.model;

/**
 *
 * @author Admin
 */
public class ModeleAvantageRetenu {

    private int id;
    private String designation;
    private double taux, montant;
    private int etat;

    private static ModeleAvantageRetenu instance;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDesignation() {
        return designation;
    }

    public void setDesignation(String designation) {
        this.designation = designation;
    }

    public double getTaux() {
        return taux;
    }

    public void setTaux(double taux) {
        this.taux = taux;
    }

    public double getMontant() {
        return montant;
    }

    public void setMontant(double montant) {
        this.montant = montant;
    }

    public int getEtat() {
        return this.etat;
    }

    public void setEtat(int etat) {
        this.etat = etat;
    }

    public static ModeleAvantageRetenu getInstance() {
        return instance == null ? new ModeleAvantageRetenu() : instance;
    }
}
